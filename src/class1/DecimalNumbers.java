package class1;

public class DecimalNumbers {

    public static void main(String[] args) {

        // There are two data types that can hold decimals are the following types: 'FLOAT' & 'DOUBLE'

        float myNumberBox = 10.5f;
        System.out.println(myNumberBox);

        double largeDecimalBox = 12345.567;
        System.out.println(largeDecimalBox);
        

    }
}
