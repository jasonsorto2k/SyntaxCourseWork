package class8;

public class Demo {

    public static void main(String[] args) {

        for (int i = 0; i < 1; i++) {
            System.out.println(" ");

            for (int j = i; j <= 5; j++) {
                System.out.print(j + " ");

            }
            System.out.println("");
            for (int h = i; h <= 10; h += 2) {
                System.out.print(h + " ");
                System.out.print("");


            }
            System.out.println("");
            for (int g = i; g <= 15; g += 3) {
                System.out.print(g + " ");
                System.out.print("");
            }
        }
    }
}



