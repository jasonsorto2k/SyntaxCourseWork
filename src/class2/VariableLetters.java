package class2;

public class VariableLetters {

    public static void main(String[] args) {

        // Letters and symbols belong to 'Char' Data Type

        char letters = 's';
        char gender = 'm';
        char symbol = '#';
        char numberChar = '1';

        // combination of letters belong to 'String' Data Type

        String name = "sam";

        // boolean data types refer to True or False statements

        boolean hungry = true;
        boolean sleepy = false;
    }
}
