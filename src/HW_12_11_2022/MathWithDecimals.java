package HW_12_11_2022;

public class MathWithDecimals {

    public static void main(String[] args) {

        double x = 25.5;
        double y = 35.5;

        System.out.println("The Addition of 2 numbers "+x+" and "+y+" is equal to " + (x + y));
        System.out.println("The Subtraction of 2 numbers 25.5 and 35.5 is equal to " + (x - y));
        System.out.println("The Multiplication of 2 numbers 25.5 and 33.5 is equal to " + (x * y));
        System.out.println("The Division of 2 numbers 25.5 and 35.5 is equal to " + (x / y));

        double z = 3.9;

        System.out.println("The square of 3.9 is " + (z * z));


    }
}
