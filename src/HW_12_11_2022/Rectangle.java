public class Rectangle {

    public static void main(String[] args) {

        // P=2h+2w   w - width
        // A=hw      h - height

        double width = 5.0;
        double height = 8.0;

        System.out.println
                ("The perimeter of a rectangle with a width of 5 and height of 8" +
                        " is equal to " +(2*height + 2*width) + " and the Area is " + (height * width));

    }
}
