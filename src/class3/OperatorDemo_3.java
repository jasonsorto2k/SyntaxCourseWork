package class3;

public class OperatorDemo_3 {
    public static void main(String[] args) {

        //REMAINDER = %

        int number = 10;

        System.out.println(number/3); //3
        System.out.println(number%3); //1

        System.out.println(16/5);
        System.out.println(16%5);

        //Remainder can show you whether a number is even or odd

        System.out.println(156%2);
        System.out.println(155%2);

        System.out.println(1555%5);

    }
}
